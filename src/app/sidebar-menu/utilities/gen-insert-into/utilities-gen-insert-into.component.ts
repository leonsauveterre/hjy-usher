  private initializeDropdown(inputId: string, menuElement: HTMLDivElement): void {
    if (!isPlatformBrowser(this.platformId)) {
      return;
    }

    const input = document.getElementById(inputId) as HTMLInputElement;
    input.addEventListener('focus', () => {
      menuElement.classList.add('show');
    });

    // Filter menu items as the user types
    input.addEventListener('input', () => {
      const query = input.value.toLowerCase();
      const items = menuElement.querySelectorAll('.dropdown-item');
      items.forEach(item => {
        const text = item.textContent?.toLowerCase() || '';
        (item as HTMLElement).style.display = text.includes(query) ? '' : 'none';
      });
    });

    // Set input value when a menu item is clicked
    menuElement.querySelectorAll('.dropdown-item').forEach(item => {
      item.addEventListener('click', (event: Event) => {
        const target = event.target as HTMLElement;
        if (target && target.textContent) {
          input.value = target.textContent.trim(); // Assign the value
          menuElement.classList.remove('show'); // Hide menu after selection
        }
      });
    });

    // Hide menu on blur
    input.addEventListener('blur', () => {
      setTimeout(() => menuElement.classList.remove('show'), 200);
    });
  }

  selectOption(event: MouseEvent, field: 'database' | 'table'): void {
    event.preventDefault();
    const selectedValue = (event.target as HTMLElement).textContent?.trim();
    if (selectedValue) {
      if (field === 'database') {
        this.updateDatabase(selectedValue);
      } else {
        this.tableName = selectedValue;
      }
    }
  }

  updateDatabase(databaseName: string): void {
    this.databaseName = databaseName;
    this.availableTables = this.databases.find(db => db.name === databaseName)?.tables || [];
    this.tableName = '';
  }

  onDatabaseNameInput(): void {
    this.updateDatabase(this.databaseName);
  }

  preventClose(event: MouseEvent): void {
    event.stopPropagation();
  }

