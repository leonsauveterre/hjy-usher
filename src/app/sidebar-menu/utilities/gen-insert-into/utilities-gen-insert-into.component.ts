  private convertToInsertStatements(
    fallback: string,
    databaseName: string,
    tableName: string,
    rawOutput: string,
    lowercaseKeywords: boolean = false,
    generateMultipleStatements: boolean = false,
    skipBrokenLines: boolean = false,
    treatNullAsText: boolean = false,
    tenantId?: number
  ): string {
    const rawLines = rawOutput.split("\n");
    const lines = rawLines.slice(1, rawLines.length - 1).map(line => line.trim());
    const columnNames = this.removeExtraSpaces(lines[0].substring(1, lines[0].length - 1).split('|')).map(e => e.trim());

    if (columnNames.some(name => name === "")) {
      this.caveat.trigger('Column names should NOT be empty.', 'error')
      return fallback;
    }

    if (columnNames.some(name => name.includes(' '))) {
      this.caveat.trigger('Column names should NOT include spaces.', 'error')
      return fallback;
    }

    const prefix = databaseName ? `${databaseName}.` : '';
    let result = `${lowercaseKeywords ? "insert into" : "INSERT INTO"} ${prefix + tableName}(${columnNames.join(", ")}) `;
    if (!generateMultipleStatements) {
      result += `${lowercaseKeywords ? "values" : "VALUES"}\n`;
    }

    const dataRows: string[][] = [];
    const tenantIdIndex = columnNames.findIndex(name => name.toLowerCase() === "tenant_id");
    const columnSize = columnNames.length;
    let skippedLines = 0;
    for (let i = 1; i < lines.length; i++) {
      const line: string = lines[i];
      if (!line || line.startsWith("+-") && line.endsWith("-+")) {
        continue;
      }

      try {
        const row: string[] = line.substring(1, line.length - 1).trim().split(/\s*\|\s*/g).map(value => {
          value = value.trim();
          if (value === "") {
            return "''";
          }
          if (value === "NULL" && treatNullAsText) {
            return "'NULL'";
          }
          return value === "NULL" ? "NULL" : `'${value.replace(/'/g, "\\'")}'`;
        });

        if (row.length !== columnSize) {
          if (skipBrokenLines) {
            ++skippedLines;
            continue;
          }
          this.caveat.trigger(`Expected ${columnSize} columns, but found ${row.length} columns on line#${i + 2}.`, 'error');
          return fallback;
        }

        // Overwrite tenant_id if applicable
        if (tenantId !== undefined && tenantIdIndex >= 0) {
          row[tenantIdIndex] = tenantId.toString();
        }

        dataRows.push(row);
      } catch (error) {
        if (skipBrokenLines) {
          continue;
        }
        throw new Error(`Error parsing line: "${line}"`);
      }
    }

    if (generateMultipleStatements) {
      result = dataRows
        .map(row => {
          const values = row.join(", ");
          return `${lowercaseKeywords ? "insert into" : "INSERT INTO"} ${tableName} (${columnNames.join(", ")}) ${lowercaseKeywords ? "values" : "VALUES"} (${values});`;
        })
        .join("\n");
    } else {
      for (const row of dataRows) {
        result += `(${row.join(", ")}),\n`;
      }
      result = result.slice(0, -2) + ";";
    }

    if (skippedLines > 0) {
      this.caveat.trigger(`Skipped ${skippedLines} broken line(s).`, 'warning');
    }
    return result;
  }

  private removeExtraSpaces(arr: string[]): string[] {
    return arr.map(item => item.replace(/\|/g, " ").trim());
  }

  genSQL(): void {
    if (isPlatformBrowser(this.platformId)) {
      const databaseName = (document.getElementById('gen-insert-into-input-2-database-name') as HTMLInputElement).value.trim();
      const tableName = (document.getElementById('gen-insert-into-input-2-table-name') as HTMLInputElement).value.trim();
      const funcPrefix = 'gen-insert-into-input-cond2-opt';
      const lowercaseKeywords = (document.getElementById(funcPrefix + '1') as HTMLInputElement).checked;
      const generateMultipleStatements = (document.getElementById(funcPrefix + '2') as HTMLInputElement).checked;
      const skipBrokenLines = (document.getElementById(funcPrefix + '3') as HTMLInputElement).checked;
      const treatNullAsText = (document.getElementById(funcPrefix + '4') as HTMLInputElement).checked;
      const tenantIdInput = document.getElementById('gen-insert-into-input-3-tenant-id') as HTMLInputElement;
      this.generatedSQL = this.convertToInsertStatements(
        this.generatedSQL,
        databaseName,
        tableName,
        this.rawOutput.nativeElement.value,
        lowercaseKeywords,
        generateMultipleStatements,
        skipBrokenLines,
        treatNullAsText,
        tenantIdInput ? parseInt(tenantIdInput.value) : undefined
      );
      this.cdr.detectChanges();
      this.codeResult.rerender();
    }
  }
